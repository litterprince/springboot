package com.example1.po;

public class PersonBean {
    private String name;
    private Integer age;

    public PersonBean() {
        super();
    }

    public PersonBean(String name, Integer age) {
        super();
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }
}
